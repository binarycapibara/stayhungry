int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int res;
	int counter;

	counter = 0;
	i = 0;
	while (tab[i])
	{
		res = f(&tab[i][0]);
		i++;
		if (res == 1)
			counter++;
	}
	return (counter);
}
